#Arithmetics Operators +, -, *, /, //, %, **
a = 12
b = 10

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(2 ** 2)

#Comaprison Operators >, <, ==, !=, >=, <=
a = 12
b = 10

print(a > b)
print(a < b)
print(a == b)
print(a != b)
print(a <= b)
print(a >= b)

#logical Operators and, or, not
bool1 = True
bool2 = False

print(bool2 == bool1)
print(not bool1)

#Assignment Operators =, +=, -=, /=, %=, //=
a = 12
b = 10

print(a + 5)
#Identity operator is , is not
bool1 = True
bool2 = False

print(bool2 is bool1)
print(bool1 is not bool2)

#Membership Operators in , not in
li = [1, 2, 3, 4]
print(2 in li)
print(5 not in li)
